code([
    "# Embedding extraction\n",
    "def extract_embeddings(model, dataloader, device):\n",
    "    \"\"\"Extract mean-pooled final-layer embeddings from the fine-tuned model.\"\"\"\n",
    "    model.eval()\n",
    "    all_embeddings = []\n",
    "    all_labels = []\n",
    "    \n",
    "    with torch.no_grad():\n",
    "        for batch in tqdm(dataloader, desc='Extracting embeddings'):\n",
    "            input_ids = batch['input_ids'].to(device)\n",
    "            attention_mask = batch['attention_mask'].to(device)\n",
    "            \n",
    "            outputs = model.base_model(\n",
    "                input_ids=input_ids,\n",
    "                attention_mask=attention_mask,\n",
    "                output_hidden_states=True,\n",
    "            )\n",
    "            \n",
    "            # Mean pool over non-padded tokens\n",
    "            hidden = outputs.hidden_states[-1]\n",
    "            mask_exp = attention_mask.unsqueeze(-1).float()\n",
    "            mean_pooled = (hidden * mask_exp).sum(1) / mask_exp.sum(1)\n",
    "            \n",
    "            all_embeddings.append(mean_pooled.cpu().numpy())\n",
    "            if 'labels' in batch:\n",
    "                all_labels.extend(batch['labels'].numpy())\n",
    "    \n",
    "    embeddings = np.vstack(all_embeddings)\n",
    "    labels = np.array(all_labels) if all_labels else None\n",
    "    return embeddings, labels\n",
    "\n",
    "\n",
    "print('extract_embeddings() defined.')\n",
    "print(f'Output shape will be: (N_chunks, {hidden_size})')\n",
])

code([
    "# PCA and stance index construction\n",
    "from sklearn.decomposition import PCA\n",
    "\n",
    "def build_stance_index(train_embeddings, train_labels, all_embeddings):\n",
    "    \"\"\"\n",
    "    Build latent policy stance index via PCA.\n",
    "    PCA fitted on TRAINING data only (no leakage).\n",
    "    \n",
    "    Returns:\n",
    "        stance_scores: projection onto PC1 for all embeddings\n",
    "        pca: fitted PCA object\n",
    "    \"\"\"\n",
    "    # Fit PCA on training embeddings ONLY\n",
    "    pca = PCA(n_components=50)  # Keep top 50 components\n",
    "    pca.fit(train_embeddings)\n",
    "    \n",
    "    print(f'PCA fitted on training embeddings:')\n",
    "    print(f'  PC1 explains {pca.explained_variance_ratio_[0]*100:.1f}% of variance')\n",
    "    print(f'  Top 5 PCs explain {pca.explained_variance_ratio_[:5].sum()*100:.1f}%')\n",
    "    print(f'  Top 10 PCs explain {pca.explained_variance_ratio_[:10].sum()*100:.1f}%')\n",
    "    \n",
    "    # Project all embeddings\n",
    "    all_projected = pca.transform(all_embeddings)\n",
    "    stance_scores = all_projected[:, 0]  # PC1 = stance dimension\n",
    "    \n",
    "    # Validate: PC1 should correlate with yield labels\n",
    "    if train_labels is not None:\n",
    "        train_projected = pca.transform(train_embeddings)\n",
    "        r, p = stats.pearsonr(train_projected[:, 0], train_labels)\n",
    "        print(f'\\n  PC1 correlation with Δy₂ (train): r={r:.4f}, p={p:.2e}')\n",
    "        \n",
    "        # Flip sign if anti-correlated (so positive = hawkish)\n",
    "        if r < 0:\n",
    "            stance_scores = -stance_scores\n",
    "            print('  (Flipped PC1 sign so positive = hawkish)')\n",
    "    \n",
    "    return stance_scores, pca\n",
    "\n",
    "\n",
    "print('build_stance_index() defined.')\n",
])

