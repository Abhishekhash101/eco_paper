code([
    "# Training loop with early stopping\n",
    "def train_model(model, train_loader, val_loader, num_epochs, lr, patience, grad_clip):\n",
    "    \"\"\"Train with early stopping on validation MSE.\"\"\"\n",
    "    optimizer = torch.optim.AdamW(\n",
    "        model.parameters(), lr=lr, weight_decay=WEIGHT_DECAY\n",
    "    )\n",
    "    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(\n",
    "        optimizer, T_max=num_epochs\n",
    "    )\n",
    "    \n",
    "    best_val_loss = float('inf')\n",
    "    patience_counter = 0\n",
    "    history = {'train_loss': [], 'val_loss': [], 'val_r': []}\n",
    "    \n",
    "    for epoch in range(num_epochs):\n",
    "        # Train\n",
    "        model.train()\n",
    "        train_losses = []\n",
    "        for batch in tqdm(train_loader, desc=f'Epoch {epoch+1}/{num_epochs}'):\n",
    "            batch = {k: v.to(device) for k, v in batch.items()}\n",
    "            outputs = model(\n",
    "                input_ids=batch['input_ids'],\n",
    "                attention_mask=batch['attention_mask'],\n",
    "                labels=batch['labels'],\n",
    "            )\n",
    "            loss = outputs['loss']\n",
    "            loss.backward()\n",
    "            torch.nn.utils.clip_grad_norm_(model.parameters(), grad_clip)\n",
    "            optimizer.step()\n",
    "            optimizer.zero_grad()\n",
    "            train_losses.append(loss.item())\n",
    "        \n",
    "        scheduler.step()\n",
    "        avg_train_loss = np.mean(train_losses)\n",
    "        \n",
    "        # Validate\n",
    "        model.eval()\n",
    "        val_losses = []\n",
    "        val_preds = []\n",
    "        val_labels = []\n",
    "        with torch.no_grad():\n",
    "            for batch in val_loader:\n",
    "                batch = {k: v.to(device) for k, v in batch.items()}\n",
    "                outputs = model(\n",
    "                    input_ids=batch['input_ids'],\n",
    "                    attention_mask=batch['attention_mask'],\n",
    "                    labels=batch['labels'],\n",
    "                )\n",
    "                val_losses.append(outputs['loss'].item())\n",
    "                val_preds.extend(outputs['prediction'].cpu().numpy())\n",
    "                val_labels.extend(batch['labels'].cpu().numpy())\n",
    "        \n",
    "        avg_val_loss = np.mean(val_losses)\n",
    "        val_r = stats.pearsonr(val_preds, val_labels)[0]\n",
    "        \n",
    "        history['train_loss'].append(avg_train_loss)\n",
    "        history['val_loss'].append(avg_val_loss)\n",
    "        history['val_r'].append(val_r)\n",
    "        \n",
    "        print(f'  Epoch {epoch+1}: train_loss={avg_train_loss:.4f}, '\n",
    "              f'val_loss={avg_val_loss:.4f}, val_r={val_r:.4f}')\n",
    "        \n",
    "        # Early stopping\n",
    "        if avg_val_loss < best_val_loss:\n",
    "            best_val_loss = avg_val_loss\n",
    "            patience_counter = 0\n",
    "            # Save best checkpoint\n",
    "            ckpt_dir = PROJECT_ROOT / 'models' / 'best_checkpoint'\n",
    "            ckpt_dir.mkdir(parents=True, exist_ok=True)\n",
    "            torch.save(model.state_dict(), ckpt_dir / 'model.pt')\n",
    "            print(f'  -> Best model saved (val_loss={best_val_loss:.4f})')\n",
    "        else:\n",
    "            patience_counter += 1\n",
    "            if patience_counter >= patience:\n",
    "                print(f'  Early stopping at epoch {epoch+1}')\n",
    "                break\n",
    "        \n",
    "        # Overfitting check\n",
    "        if avg_val_loss > 2 * avg_train_loss:\n",
    "            print(f'  WARNING: val_loss > 2x train_loss — possible overfitting!')\n",
    "    \n",
    "    return history\n",
    "\n",
    "\n",
    "# Run training (only if labels available)\n",
    "if HAS_LABELS:\n",
    "    print('Starting training...')\n",
    "    history = train_model(\n",
    "        model, train_loader, val_loader,\n",
    "        num_epochs=NUM_EPOCHS, lr=LEARNING_RATE,\n",
    "        patience=PATIENCE, grad_clip=GRAD_CLIP,\n",
    "    )\n",
    "else:\n",
    "    print('Skipping training — no labels available.')\n",
    "    history = None\n",
])

