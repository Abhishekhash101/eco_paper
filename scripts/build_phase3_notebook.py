code([
    "# Model wrapper: base LLM + LoRA + regression head\n",
    "class PolicyStanceRegressor(nn.Module):\n",
    "    \"\"\"LLM with LoRA adapter and linear regression head.\"\"\"\n",
    "    \n",
    "    def __init__(self, base_model, hidden_size):\n",
    "        super().__init__()\n",
    "        self.base_model = base_model\n",
    "        self.regression_head = nn.Linear(hidden_size, 1)\n",
    "        self.dropout = nn.Dropout(0.1)\n",
    "    \n",
    "    def forward(self, input_ids, attention_mask, labels=None):\n",
    "        # Get hidden states from base model\n",
    "        outputs = self.base_model(\n",
    "            input_ids=input_ids,\n",
    "            attention_mask=attention_mask,\n",
    "            output_hidden_states=True,\n",
    "        )\n",
    "        \n",
    "        # Use last hidden state, mean-pool over non-padded tokens\n",
    "        hidden_states = outputs.hidden_states[-1]  # (batch, seq_len, hidden)\n",
    "        mask_expanded = attention_mask.unsqueeze(-1).float()\n",
    "        sum_hidden = (hidden_states * mask_expanded).sum(dim=1)\n",
    "        count = mask_expanded.sum(dim=1)\n",
    "        mean_pooled = sum_hidden / count  # (batch, hidden)\n",
    "        \n",
    "        # Regression\n",
    "        pooled = self.dropout(mean_pooled)\n",
    "        prediction = self.regression_head(pooled).squeeze(-1)  # (batch,)\n",
    "        \n",
    "        loss = None\n",
    "        if labels is not None:\n",
    "            loss_fn = nn.MSELoss()\n",
    "            loss = loss_fn(prediction, labels)\n",
    "        \n",
    "        return {'loss': loss, 'prediction': prediction}\n",
    "\n",
    "\n",
    "print('PolicyStanceRegressor class defined.')\n",
])

